    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        double[] function = new double[6];
        double x = 2;

        function[0] = Double.parseDouble(jTextField7.getText());
        function[1] = Double.parseDouble(jTextField6.getText());
        function[2] = Double.parseDouble(jTextField1.getText());
        function[3] = Double.parseDouble(jTextField5.getText());
        function[4] = Double.parseDouble(jTextField4.getText());
        function[5] = Double.parseDouble(jTextField3.getText());

        double y = funcVal(x, function);

        jLabel2.setText(String.valueOf(y));
        
        //Draw axes
        jPanel2.setVisible(true);
        Graphics g = jPanel2.getGraphics();
        g.setColor(Color.black);
        g.drawLine(250, 0, 250, 500);
        g.drawLine(0, 250, 500, 250);
    
        int xTickNum = 10;
        for (int i = 0; i<=500; i+=25) {
            String tickText = String.valueOf(xTickNum);
            g.drawString(tickText, 233, i+10);
            g.drawLine(250, i, 245, i);
            g.drawLine(250, i, 255, i);
            xTickNum--;
            
        }
        for (int i = 0; i<=500; i+=25) {
            g.drawLine(i, 250, i, 245);
            g.drawLine(i, 250, i, 255);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

