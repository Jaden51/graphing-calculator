    private static double funcVal(double xIn, double arrayIn[]) {
        double y = 0;
        for (int i = 0; i < arrayIn.length; i++) {
            System.out.println(arrayIn[i] + " " + i);
            y += arrayIn[i] * (Math.pow(xIn, i));
        }
        return y;
    }

